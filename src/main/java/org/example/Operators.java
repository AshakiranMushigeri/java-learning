package org.example;

public class Operators {
    public static void main(String[] args) {
        int a =3;
        a=a+2;
        System.out.println(a);
        a+=2;
        System.out.println(a);

        int c =5;
        c = c%3;
        System.out.println(c);
        c%=3;
        System.out.println(c);

    }
}
